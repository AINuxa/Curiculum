let languageCode = "en"; // Change different languages

switch (languageCode) {
  case "en":
    console.log("Welcome!"); // English
    break;
  case "fr":
    console.log("Bienvenue!"); // French
    break;
  case "es":
    console.log("Bienvenido!"); // Spanish
    break;
  default:
    console.log("Welcome!"); // Default to English
    break;
}